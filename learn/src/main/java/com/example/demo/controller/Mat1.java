package com.example.demo.controller;



public class Mat1 {
int x,y;
int z;


	public  Mat1(int num1, int num2) {
		
		
		x=num1;
		y=num2;

}

	public int add1() {
		z=x+y;
		return(z);
		
	}

public int sub1() {
		
		return(x-y);
		
	}
	


public int mul1() {
	
	return(x*y);
	
}


public int div1() {
	
	return(x/y);
	
}
	
}
